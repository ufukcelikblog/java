/*
public: erişime açık
class: sınıf belirtiyor
UygulamaBir: sınıf adı = dosya adı(case-sensitive)
*/
public class UygulamaBir { // sınıf başlangıcı
    /* ana metot 
        mutlaka public
        mutlaka static (hafızaya yerleşik)
        mutlaka void (geriye değer döndürmeyen)
        bütün sınıflar, metotlar, döngüler, 
        kontrol işlemleri süslü parantezle 
        başlar ve biter
    */
    public static void main(String[] args) {
        System.out.println("Merhaba JAVA !!!");
    }
} // sınıf bitişi
