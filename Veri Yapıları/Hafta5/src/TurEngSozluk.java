  private void jButton_ARAMAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ARAMAActionPerformed
    String adres = "C:\\Users\\ucelik\\Documents\\sozluk.txt";
    String sonuc = "";
    String arananKelime = jTextField_ARAMA.getText();
    String kayit = "";
    String Tur, Eng;
    try {
      Scanner okuyucu = new Scanner(new File(adres));
      while(okuyucu.hasNext()) {
        kayit = okuyucu.next();
        if(jRadioButton_TUMKELIME.isSelected()) {
          Tur = kayit.substring(0, kayit.indexOf("="));
          Eng = kayit.substring(kayit.indexOf("=")+1, kayit.length());
          if(arananKelime.equalsIgnoreCase(Tur)) {
            sonuc += "Aranan : " + arananKelime + " = " + Eng + "\n";
          } else if(arananKelime.equalsIgnoreCase(Eng)) {
            sonuc += "Searched : " + arananKelime + " = " + Tur + "\n";
          }
        } else {
          // içinde geçen kelimeleri alır
          if(kayit.contains(arananKelime)) {
            sonuc += arananKelime + " -> " + kayit + "\n";
          }
        }        
      }
      if(sonuc.equals("")) sonuc = "Aranan kelime bulunamadı";
      jTextArea_ARAMA.setText(sonuc);
    } catch (FileNotFoundException ex) {
      JOptionPane.showMessageDialog(null, "Hata: " + ex.toString());
    }  
  }//GEN-LAST:event_jButton_ARAMAActionPerformed
  
