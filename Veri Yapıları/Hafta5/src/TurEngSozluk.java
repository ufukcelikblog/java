  private void jButton_SILMEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SILMEActionPerformed
    String adres = "C:\\Users\\ucelik\\Documents\\sozluk.txt"; 
    String secilenKelime = jList_SILME.getSelectedValue();
    System.out.println("Seçilen Kelime = " + secilenKelime);
    String kelimeler = "";
    try {
      Scanner okuyucu = new Scanner(new File(adres));
      while(okuyucu.hasNext()) {
        String kayit = okuyucu.next();
        if(!kayit.equals(secilenKelime)) {
          kelimeler += kayit + "\n";
        }
      }
      okuyucu.close();
    } catch (FileNotFoundException ex) {
      JOptionPane.showMessageDialog(null, "Hata: " + ex.toString());
    }
    System.out.println(kelimeler);
    try {
      File dosya = new File(adres);
      FileWriter yazici = new FileWriter(dosya);
      BufferedWriter yaz = new BufferedWriter(yazici);
      yaz.write(kelimeler);
      yaz.newLine();
      yaz.close();
    } catch (IOException ex) {
      JOptionPane.showMessageDialog(null, "Hata: " + ex.toString());
    }
  }//GEN-LAST:event_jButton_SILMEActionPerformed

