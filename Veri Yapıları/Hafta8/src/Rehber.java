    public void KayitGetir(String yon) {
        try {
            switch(yon) {
                case "ileri" : res.next(); break;
                case "geri" : res.previous(); break;
                case "ilk" : res.first(); break;
                case "son" : res.last(); break;
                case "mutlak" : res.absolute(Integer.parseInt(jTextField_ID.getText()));
            }
            
            jTextField_AD.setText(res.getString("ad"));
            jTextField_SOYAD.setText(res.getString("soyad"));
            jLabel_YAS.setText("" + res.getInt("yas"));
            jSlider_YAS.setValue(res.getInt("yas"));
            if(res.getInt("cinsiyet") == 0) {
                jRadioButton_ERKEK.setSelected(true);
                jRadioButton_ERKEK.setFont(new Font("Tahoma", Font.BOLD, 13));
                jRadioButton_KADIN.setFont(new Font("Tahoma", Font.PLAIN, 12));
            } else {
                jRadioButton_KADIN.setSelected(true);
                jRadioButton_KADIN.setFont(new java.awt.Font("Tahoma", Font.BOLD, 12)); 
                jRadioButton_ERKEK.setFont(new java.awt.Font("Tahoma", Font.PLAIN, 12));
            }   
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Kayıt YOK");
        }
        
    }
    public Statement baglantiAc() throws Exception {
        Class.forName(driver).newInstance();
        conn = DriverManager.getConnection(url + dbName, userName, password);
        return conn.createStatement(
                ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE
        );
    }
            
