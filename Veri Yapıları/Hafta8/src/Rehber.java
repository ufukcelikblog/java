    private void jButton_GUNCELLEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GUNCELLEActionPerformed
        try {
            res.updateString("ad", jTextField_AD.getText());
            res.updateString("soyad", jTextField_SOYAD.getText());
            res.updateInt("yas", Integer.parseInt(jLabel_YAS.getText()));
            if(jRadioButton_ERKEK.isSelected()) {
                res.updateInt("cinsiyet", 0);
            } else {
                res.updateInt("cinsiyet", 1);
            }
            res.updateRow();
            JOptionPane.showMessageDialog(null, "Kayıt Güncellendi");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Güncelleme BAŞARISIZ !!!");
        } 
    }//GEN-LAST:event_jButton_GUNCELLEActionPerformed

    private void jButton_EKLEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EKLEActionPerformed
        try {
            res.moveToInsertRow(); // Kaydın ekleneceği yeni satıra git
            res.updateString("ad", jTextField_AD.getText());
            res.updateString("soyad", jTextField_SOYAD.getText());
            res.updateInt("yas", Integer.parseInt(jLabel_YAS.getText()));
            if(jRadioButton_ERKEK.isSelected()) {
                res.updateInt("cinsiyet", 0);
            } else {
                res.updateInt("cinsiyet", 1);
            }
            res.insertRow(); // kaydı (satırı) tabloya ekle
            JOptionPane.showMessageDialog(null, "Kayıt eklendi");
            res.close();
            res = st.executeQuery("SELECT * FROM kisi");
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Kayıt ekleme BAŞARISIZ !!!");
        }
    }//GEN-LAST:event_jButton_EKLEActionPerformed

